						<h3>Contexte</h3>
						<div class="text_columns_2">
							<p>La commune de Saint-André, dans les Pyrénées-Orientales, se situe entre le littoral touristique d’Argelès-Sur-Mer et le massif des Albères. Elle abrite un viticulteur bio, des terres maraichères et arboricoles et quelques espaces de pâturage. La municipalité se mobilise pour valoriser les produits locaux et sensibiliser les citoyens aux problématiques agro-environnementales : organisation de marchés (bio et de producteurs), ateliers pédagogiques, mise en place d’une grainothèque et d’un plan zéro-phyto… Toutefois, l’agriculture est en recul. Le parcellaire est morcelé et certains propriétaires pratiquent la rétention foncière dans l’espoir de vendre leurs terrains pour l’urbanisation. Ce contexte est propice au développement de friches, qui dégradent les paysages (parcelles abandonnées, dépôts d’ordure) et accentuent les risques d’incendie.</p>
						</div>
