						<h3>Impulsion et démarche</h3>
						<div class="text_columns_2">
							<h6>L’agenda 21 prévoit la revalorisation des friches</h6>
							<p>En 2014, Saint-André commence à élaborer un agenda 21. L’un des enjeux identifiés est la revalorisation des terres en friches. Encouragée par l’exemple voisin de Claira (cf. fiche expérience « La reconquête des friches à Claira »), la mairie veut remobiliser les parcelles délaissées au profit d’éleveurs locaux souhaitant cultiver du fourrage bio. Cela présente plusieurs avantages : améliorer l’autonomie alimentaire des troupeaux, redynamiser l’économie locale, stopper la dégradation des terrains... En 2015, un stagiaire de la commune établit un premier recensement des friches et les classe selon leur état plus ou moins boisé, arbustif, ou herbacé. La mairie prévoit de faciliter les échanges de parcelles en jouant un rôle d’intermédiaire : les propriétaires lui confient leurs terres, qu’elle mettra ensuite à disposition des éleveurs par des conventions pluriannuelles.</p>
							<h6>Une animation foncière dynamique</h6>
                            <p>Saint-André lance des actions de communication auprès des propriétaires fonciers. Une réunion est organisée pour présenter les résultats du recensement des friches. En amont, la mairie a envoyé des courriers pour rappeler les obligations d’entretien des parcelles dans certaines zones. Elle communique aussi au travers de bulletins d’information locaux et de la tenue de stands pour susciter l’adhésion des propriétaires au projet. Petit à petit, la mairie obtient la maîtrise d’une vingtaine d’hectares sous forme de prêts civils d’une durée de cinq ans, à reconduction tacite, pour expérimenter les cultures fourragères. Elle convie les éleveurs locaux à un arpentage des terrains en compagnie de la Chambre d’agriculture. Les éleveurs formulent leurs vœux pour telle ou telle parcelle qu’ils s’engagent à entretenir. La qualité des terres est hétérogène. Pour certaines qui nécessitent d’importants travaux de réhabilitation, la mairie envisage de réaliser les premières actions de débroussaillage et d’entretien, notamment en autorisant le pâturage de chèvres.</p>
                        </div>
                        <blockquote>
                            <p>Saint-André lance des actions de communication auprès des propriétaires fonciers. Une réunion est organisée pour présenter les résultats du recensement des friches. En amont</p> <cite>Maria Sanguely, élue municipale de Saint André</cite> </blockquote>
                        <h6>De la culture de fourrage à un projet d’installation</h6>
                        <p>Des parcelles au nord de la commune sont toutefois propices à la remise en culture. La mairie les confie à un éleveur, mais il se retire pour raisons personnelles en 2016. Un jeune agriculteur reste intéressé pour revaloriser les friches. Il souhaite s’installer en maraichage et développer une activité d’élevage de poules et de chèvres. L’agriculteur possède 1,5 ha et la municipalité décide de lui confier 18,5 ha additionnels pour favoriser l’entretien progressif des parcelles. Pour faciliter la construction d’un siège d’exploitation abritant une fromagerie, la mairie accepte de réviser son plan local d’urbanisme (PLU) afin d’autoriser l’implantation de bâti agricole au sud du village. Le candidat à l’installation prend contact avec des associations (Terre de Liens, Terres Vivantes) pour l’aider à développer son projet. </p>
                        <h3>Résultats</h3>
                        <p>L’animation foncière de Saint-André a permis de convaincre une quinzaine de propriétaires de confier leurs parcelles à la commune. Plusieurs facteurs rendent difficile la valorisation agricole des friches. Toutefois, la démarche permet de sensibiliser les propriétaires à la nécessité d’entretenir les terrains et favorise l’installation d’un agriculteur. La modification du PLU, depuis longtemps en discussion, se concrétise à l’automne 2017. Avec l’aide de Terres Vivantes, l’agriculteur prévoit de débuter son activité de maraichage en septembre sur les terres qu’il possède et de déposer un permis de construire pour la construction de la fromagerie en octobre. S’il l’obtient, il pourra semer du fourrage en 2018, puis entamer dès 2019 l’activité d’élevage de chèvres qui aidera à poursuivre les actions d’entretien des parcelles.</p>
						<h3>Témoignages</h3>
						<blockquote>
							<p>Nous avons pu convaincre un nombre croissant de propriétaires de participer au projet en renouvelant les actions de communication. Chaque nouvel article dans le bulletin municipal, courrier, ou réunion publique a permis de remporter de nouvelles adhésions. Cela participe aussi à la sensibilisation des habitants au problème des friches.</p> <cite>Chargée de mission Agenda 21, Mairie de St andré</cite> </blockquote>
